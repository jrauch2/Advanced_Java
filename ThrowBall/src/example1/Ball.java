package example1;

/**
 * This class is a software abstraction of a real ball.
 *
 * @author jlombardo
 */
public class Ball {

    /**
     * This method is inherited from the 'Object' class and is overridden
     * to provide the name of the object.
     *
     * @return the name of the object
     */
    @Override
    public String toString() {
        return "Ball";
    }
}
